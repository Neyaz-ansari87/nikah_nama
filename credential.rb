require 'rack/test'

describe 'CORS configuration' do
  include Rack::Test::Methods

  def app
    Rails.application
  end

  it 'allows cross-origin requests' do
    options '/example', headers: {
      'Origin' => 'https://example.com',
      'Access-Control-Request-Method' => 'GET'
    }

    expect(last_response.status).to eq(204)
    expect(last_response.headers['Access-Control-Allow-Origin']).to eq('*')
    expect(last_response.headers['Access-Control-Allow-Methods']).to eq('GET, POST, PUT, PATCH, DELETE, OPTIONS')
    expect(last_response.headers['Access-Control-Allow-Headers']).to eq('Origin, Content-Type, Accept, Authorization, Token')
  end

  it 'restricts cross-origin requests to specified origins' do
    options '/example', headers: {
      'Origin' => 'https://example.com',
      'Access-Control-Request-Method' => 'GET'
    }

    expect(last_response.status).to eq(204)
    expect(last_response.headers['Access-Control-Allow-Origin']).to eq('https://example.com')
    expect(last_response.headers['Access-Control-Allow-Methods']).to eq('GET, POST, PUT, PATCH, DELETE, OPTIONS')
    expect(last_response.headers['Access-Control-Allow-Headers']).to eq('Origin, Content-Type, Accept, Authorization, Token')
  end
end





